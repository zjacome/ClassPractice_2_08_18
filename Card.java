
public class Card {
int number;
String suit;
	public Card(int number, String suit) {
		this.number=number;
		this.suit=suit;
		
	

}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getSuit() {
		return suit;
	}
	public void setSuit(String suit) {
		this.suit = suit;
	}
	
}